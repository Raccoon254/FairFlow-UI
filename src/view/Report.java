    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        ArrayList<ArrayList<String>> comboItems = new Operations().getAllTransactions();
        // iterate through the outer ArrayList and add the second item in each inner
        // ArrayList to the combo box
        // empty org before adding data
        transaction.removeAllItems();
        for (ArrayList<String> row : comboItems) {
            String item = row.get(0);
            transaction.addItem(item);
        }
    }//GEN-LAST:event_formWindowActivated
