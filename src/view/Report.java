    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //Add a report to the database
        String descriptionText = description.getText();
        String caseName = name.getText();
        String transactionId = (String) transaction.getSelectedItem();
        String userId = user.getText();

        if(descriptionText.equals("") || caseName.equals("") || transactionId.equals("") || userId.equals("")){
            JOptionPane.showMessageDialog(null, "Please fill all the fields");
        } else {
            int result = JOptionPane.showConfirmDialog(null, "Do you want to proceed?", "Confirmation", JOptionPane.YES_NO_OPTION);
            
        if (result == JOptionPane.YES_OPTION) {
            // user clicked Yes
            System.out.println(new Operations().reportCorruption(caseName,transactionId,descriptionText,userId));
             JOptionPane.showMessageDialog(null, "Case Report Success");
             new Dashboard().setVisible(true);
             dispose();
             /*-----------------------------------*/
             
        } else if (result == JOptionPane.NO_OPTION) {
            // user clicked No
            JOptionPane.showMessageDialog(null, "You have cancelled the operation");
        } else {
            // user closed the dialog without clicking Yes or No
            JOptionPane.showMessageDialog(null, "You have cancelled the operation");
        }
        }
        
       
    }//GEN-LAST:event_jButton1ActionPerformed

