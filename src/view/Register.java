    private void formWindowActivated(java.awt.event.WindowEvent evt) {// GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        // Auto Fill ComboBox
        ArrayList<ArrayList<String>> comboItems = new Operations().getAllOrganizations();
        // iterate through the outer ArrayList and add the second item in each inner
        org.removeAllItems();
        for (ArrayList<String> row : comboItems) {
            String item = row.get(0);
            org.addItem(item);
        }

    }// GEN-LAST:event_formWindowActivated

