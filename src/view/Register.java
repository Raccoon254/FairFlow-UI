    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String username = uname.getText();
        String email = this.email.getText();
        String password = pass.getText();
        String organization = (String) org.getSelectedItem();
        String accountType = "user";
        // System.out.println( new Operations().getAllOrganizations());

        if (username.equals("")) {
            JOptionPane.showMessageDialog(null, "Username is required");
        } else if (email.equals("")) {
            JOptionPane.showMessageDialog(null, "Email is required");
        } else if (password.equals("")) {
            JOptionPane.showMessageDialog(null, "Password is required");
        } else if (organization.equals("")) {
            JOptionPane.showMessageDialog(null, "Organization is required");
        } else {
            // proceed with registration
            if (new Operations().registerUser(username, accountType, password, email, organization)) {
                JOptionPane.showMessageDialog(null, "Registration Successful");
                new Login().setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Registration Failed");
            }

            System.out.println("REGISTER RUN SUCCESS :: 200 OK");
        }

    }// GEN-LAST:event_jButton1ActionPerformed

